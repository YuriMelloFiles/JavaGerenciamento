    private void jTusuarioKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTusuarioKeyReleased
        // TODO add your handling code here:
        if (jTusuario.getSelectedRow()!= -1 )  {
            txtLogin.setText(jTusuario.getValueAt(jTusuario.getSelectedRow(), 1).toString());
            
            txtSenha.setText(jTusuario.getValueAt(jTusuario.getSelectedRow(), 2).toString());
            txtTipo_login.setSelectedItem(jTusuario.getValueAt(jTusuario.getSelectedRow(), 3).toString());
            
        }
        
        
        
    }//GEN-LAST:event_jTusuarioKeyReleased

    private void ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirActionPerformed
        // TODO add your handling code here:
        if (jTusuario.getSelectedRow()!= -1 ){
            
            Usuario h = new Usuario();
            UsuarioDAO dao = new UsuarioDAO();

            
            h.setId((int)jTusuario.getValueAt(jTusuario.getSelectedRow(), 0));
            
            dao.delete(h);
            txtLogin.setText("");
            
            txtSenha.setText("");
            
            readJTable();           

        
        } else{
        
                JOptionPane.showMessageDialog(null,"Selecione um item para excluir.");
        
        }
        
        
    }//GEN-LAST:event_ExcluirActionPerformed

    private void jTusuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTusuarioMouseClicked
        // TODO add your handling code here:
        
        if (jTusuario.getSelectedRow()!= -1 )  {
            txtLogin.setText(jTusuario.getValueAt(jTusuario.getSelectedRow(), 1).toString());
            
            txtSenha.setText(jTusuario.getValueAt(jTusuario.getSelectedRow(), 2).toString());
            txtTipo_login.setSelectedItem(jTusuario.getValueAt(jTusuario.getSelectedRow(), 3).toString());
        }
    }//GEN-LAST:event_jTusuarioMouseClicked

    private void AtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AtualizarActionPerformed
        // TODO add your handling code here:
        if (jTusuario.getSelectedRow()!= -1 ){
            
            Usuario h = new Usuario();
            UsuarioDAO dao = new UsuarioDAO();

            h.setLogin(txtLogin.getText());
            h.setSenha(txtSenha.getText());
            h.setTipo_login(txtTipo_login.getSelectedItem().toString());
            
            h.setId((int)jTusuario.getValueAt(jTusuario.getSelectedRow(), 0));
            
            dao.update(h);
            txtLogin.setText("");    
            txtSenha.setText("");            
            readJTable();
        
        }
    }//GEN-LAST:event_AtualizarActionPerformed

