    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTusuario.getModel();
        modelo.setNumRows(0);
        UsuarioDAO pdao = new UsuarioDAO();
        
        for (Usuario h: pdao.read()) {
            modelo.addRow(new Object[]{
               h.getId(),
               h.getLogin(),
               h.getSenha(),
               h.getTipo_login(),
              
            });
        }
        
    
    }

